package Datatypes;

/**
* contains necessary informations about TimeData
*@author Group_13
* */

public class TimeData {
    private int hour;
    private int minute;
    private int second;

    public TimeData(){

    }

    public TimeData(int hour, int minute, int second){
        this.hour = hour;
        this.minute = minute;
        this.second = second;
    }

    public int getHour() {
        return hour;
    }

    public int getMinute() {
        return minute;
    }

    public int getSecond() {
        return second;
    }

    public void setHour(int hour) {
        this.hour = hour;
    }

    public void setMinute(int minute) {
        this.minute = minute;
    }

    public void setSecond(int second) {
        this.second = second;
    }
}
